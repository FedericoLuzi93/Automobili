package it.luzi.automobili.dto;

public class StudenteLombokDTO 
{

		private int id;
		private String nome;
		private int eta;
		
		public StudenteLombokDTO() 
		{
			
		}
		
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getNome() {
			return nome;
		}
		public void setNome(String nome) {
			this.nome = nome;
		}
		public int getEta() {
			return eta;
		}
		public void setEta(int eta) {
			this.eta = eta;
		}
		
		
}



